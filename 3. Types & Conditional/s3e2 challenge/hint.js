// the syntax for if/else statemens:
if (firstCondition) {
    // do something
} 
else if (secondCondition) {
    // do something else
} 
else {
    // if neither firstCondition og secondCondition is truthy, then do this
} 

// the syntax for switch statements
switch (someVariable) {
  
  case "someValue":
    // do something
  break;
  
  case 'someOtherValue':
    // do something else
  break;
  
  default:  
    // in all other cases, do this!
}

